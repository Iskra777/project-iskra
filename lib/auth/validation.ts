import { z } from "zod";

// Нормалізація в нижній регістр — і реєстрація, і логін мають використовувати
// цю саму схему, інакше нормалізація лише з одного боку нічого не вирішує
// (lib/auth/authenticate.ts раніше мав TODO саме про цю прогалину).
export const emailSchema = z
  .string()
  .trim()
  .email()
  .transform((value) => value.toLowerCase());

export const usernameSchema = z
  .string()
  .trim()
  .min(3, "Мінімум 3 символи")
  .max(20, "Максимум 20 символів")
  .regex(/^[a-zA-Z0-9_]+$/, "Лише латинські літери, цифри та підкреслення");

// Мінімум довжини, без примусової складності (великі літери/цифри/символи) —
// сучасна рекомендація (NIST SP 800-63B): примусова складність історично
// веде до передбачуваних патернів, а не кращої безпеки. Максимум — запобіжник
// проти надто довгого рядка на повільне argon2-хешування.
export const passwordSchema = z
  .string()
  .min(8, "Мінімум 8 символів")
  .max(128, "Максимум 128 символів");
